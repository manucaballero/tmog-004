package org.tfoc;

/**
 * Definition for singly-linked list.
 */
public class ListNode {

    Integer val;
    ListNode next;

    ListNode() {}

    ListNode(int val) {

        this.val = val;
    }

    ListNode(Integer val, ListNode next) {

        this.val = val;
        this.next = next;
    }

}
